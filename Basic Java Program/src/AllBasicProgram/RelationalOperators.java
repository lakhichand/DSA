package AllBasicProgram;

public class RelationalOperators {

	public static void main(String[] args) {
		
		System.out.println(10==10); //true
		
		System.out.println(10==45); //false 
		
		System.out.println(2<5);//true
		
		System.out.println(2>5);//false
		
		System.out.println(2.2>2.19);//true
		
		System.out.println(10<=11);//true
		
		System.out.println(10>=10.1);//false
		
		System.out.println(3.00>3);//false
		
		System.out.println(10.00==10);//true
		
		System.out.println('a' >97);//false
		

		System.out.println(5!=5);//false
		
	
		
		
		System.out.println(true != false);//true
		
		
	    System.out.println("india"=="india"); //true
		
	}
}

package BasicProgram;

public class Narrow1Double {
	public static void main(String[] args) {
		
	
	
	double a = (double)7441.85f;  // float
	
	double b = (double)45787l;    // long
	
	double c = (double)'d';     //char
	
	double e = (double)123;   //int
	
	double f = (double)200;   //short
	
	double r = (double)-112;   // byte
	
	System.out.println(a);
	System.out.println(b);
	System.out.println(c);
	System.out.println(e);
	System.out.println(f);
	System.out.println(r);
	

	
  }
}

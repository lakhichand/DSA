package BasicProgram;

public class Wid7 {
	public static void main(String[] args) {
		
		double a = 10;  // int
		double b = -20;   //byte
		double c = 100;   // short
		double d = 'c';   //char
		double f = 100l;    // long
		double e = 4578.45f; //float
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(f);
		System.out.println(e);
	}

}
